#include "fdf.h"

int		key(int kc, void *v_e)
{
	t_en *e;

	e = (t_en *)v_e;
	kc == 53 ? exit(0) : NULL;
	if (kc == 69)
		e->z = e->z * 1.1;
	if (kc == 78)
		e->z = e->z * 0.9;
	if (kc == 15)
		e->c_r = (e->c_r <= 0 ? 255 : e->c_r - 15);
	if (kc == 5)
		e->c_g = (e->c_g <= 0 ? 255 : e->c_g - 15);
	if (kc == 11)
		e->c_b = (e->c_b <= 0 ? 255 : e->c_b - 15);
	kc == 123 ? e->mx -= 10 : 0;
	kc == 124 ? e->mx += 10 : 0;
	kc == 125 ? e->my -= 10 : 0;
	kc == 126 ? e->my += 10 : 0;
	return (0);
}
